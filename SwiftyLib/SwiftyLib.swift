import UIKit

public final class SwiftyLib {
    let name = "SwiftyLib"
    
    public init(){
        
    }
    
    public func add(a: Int, b: Int) -> Int {
        return a + b
    }
    
    public func sub(a: Int, b: Int) -> Int {
        return a - b
    }
    
    public func mult(a: Int, b: Int) -> Int {
        return a * b
    }
    
    public func div(a: Int, b: Int) -> Int {
        return a / b
    }
}
